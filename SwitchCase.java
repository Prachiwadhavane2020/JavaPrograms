package First;

public class SwitchCase {

	public static void main(String[] args)
	{
		switch (1) {
		case 1:
			System.out.println("launching chrome browser");
			break;
		case 2:
			System.out.println("launching chrome internet explorer");
			break;
		case 3:
			System.out.println("launching chrome safari");
			break;
		case 4:
			System.out.println("launching chrome firefox");
			break;
		default:
			System.out.println("your selection is wrong");
		}
	}
}
